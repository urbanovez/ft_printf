#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*ne;
	int		i;

	i = 0;
	ne = (char*)b;
	while (len)
	{
		ne[i] = (char)c;
		i++;
		len--;
	}
	return (b);
}
