#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*t;
	size_t	i;

	i = 0;
	if ((t = malloc(size + 1)) == NULL)
		return (NULL);
	while (i <= size)
	{
		t[i] = '\0';
		i++;
	}
	return (t);
}
