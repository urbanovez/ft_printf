#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*cur1;
	char	*cur2;

	cur1 = s1;
	cur2 = (char *)s2;
	while (*cur1 != '\0')
	{
		cur1++;
	}
	while (*cur2 != '\0' && n > 0)
	{
		*cur1 = *cur2;
		cur1++;
		cur2++;
		n--;
	}
	*cur1 = '\0';
	return (s1);
}
